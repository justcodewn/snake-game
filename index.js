//function for drawing 1 wall/tile item
function drawWallItem(item,x,y){
    return new Sprite({
        height:wallSize,
        width:wallSize,
        color: item===1?"#a14720":item===2?"#b55b35":item===3?"red":item===0?"#5dc257":"blue",
        location:{
            x:x*wallSize,
            y:y*wallSize,
        }
    });
}

const tile = new TileMap({
    map,
    drawCallback:(item,x,y,mapRef)=>{
        mapRef.push(drawWallItem(item,x,y));
    }
})

let iteration =0;
function getIndexFromLocation({sprite}){
    const indexes = {x:0,y:0};
    indexes.x = sprite.location.x/10;
    indexes.y = sprite.location.y/10;
    return indexes;
}
let play = true;

function detectSelfCollision(){
    let snakeSection = tail;
    do{
        if(detectCollision({
            boundA:{
                height:10,
                width:10,
                x:snakeSection.location.x,
                y:snakeSection.location.y
            },
            boundB:{
                height:10,
                width:10,
                x:head.location.x,
                y:head.location.y,
            }
        })){
            play = false;
        }
        snakeSection = snakeSection.next;
    }while(snakeSection.next);
}
function detectTileCollision(){
    const {x,y}= getIndexFromLocation({sprite:head});
    if(map[y][x]==1||map[y][x]===2){
        play = false;
    }else if(map[y][x]===3){
        tile.map[y][x]=0;
        tail.next = new SnakeBody({
            next:tail.next,
            color: COLORS[randomNumber(0,COLORS.length)],
            height:10,
            width:10,
            location:{x:tail.location.x, y:tail.location.y},
            head,
        });
        let randX=0;
        let randY=0;
        do{
            randX = randomNumber(1,map.length-2);
            randY = randomNumber(1,map[0].length-2);
        }while(tile.map[randX][randY]!==0);
        tile.map[randX][randY]=3;
        tile.rebuild();
    }

}


function draw(){
    ctx.fillStyle = "#5dc257";
    ctx.fillRect(0,0,canvas.width,canvas.height);
    tile.draw();
    tail.draw();//the tail will tell the next snake body to draw
    iteration++;
}

function update(){
    iteration %= 5;
    if(iteration===0){
        detectSelfCollision();
        switch(consumeKey()){
            case KEYS.ARROW_RIGHT:
                if(head.velocity.x===0){
                    head.velocity={y:0,x:speed}
                }
                break;
            case KEYS.ARROW_LEFT:
                if(head.velocity.x===0){
                    head.velocity={y:0,x:-speed}
                }
                break;
            case KEYS.ARROW_UP:
                if(head.velocity.y===0){
                    head.velocity={x:0,y:-speed}
                }
                break;
            case KEYS.ARROW_DOWN:
                if(head.velocity.y===0){
                    head.velocity={x:0,y:speed}
                }
                break;
        }
        tail.update();
        tile.update();
        detectTileCollision();
    }
}

//game loop
function gameLoop(){
    if(play){
        window.requestAnimationFrame(gameLoop);
        draw();
        update();
    }
}


window.addEventListener('keydown',(event)=>{
    switch(event.key){
        case 'ArrowRight':
            produceKey(KEYS.ARROW_RIGHT);
            break;
        case 'ArrowLeft':
            produceKey(KEYS.ARROW_LEFT);
            break;
        case 'ArrowUp':
            produceKey(KEYS.ARROW_UP);
            break;
        case 'ArrowDown':
            produceKey(KEYS.ARROW_DOWN);
            break;
    }
});

gameLoop();













